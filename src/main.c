#include "../includes/philo.h"

int	main(int argc, char **argv)
{
	t_sim *table;

	if (argc < 5 || argc > 6)
		return (error_argv(), EXIT_FAILURE);
	table = malloc(sizeof(t_sim));
	if (!table)
		return (EXIT_FAILURE);
	parse_data(argv, table);
	free(table);
	return (EXIT_SUCCESS);
}